function generateCaList() {
    const ca_list = [];
    ca_data.forEach((section, index) => {
        ca_list.push({num: `Content Area ${index + 1}`, question_name: "", question_url: "", answer_name: "", answer_url: ""});
        section.items.forEach(item => {
            ca_list.push(item);
        });
    });
    return ca_list;
}

